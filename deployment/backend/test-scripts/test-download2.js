const db = require('./config/database');
const axios = require('axios');

(async () => {
  try {
    // 먼저 사용자 확인
    const user = await db('users')
      .select('user_id', 'email', 'role')
      .where('email', 'teacher@example.com')
      .first();
    
    console.log('🔍 사용자 정보:', user ? user.email : '없음');
    
    // status가 completed인 최신 보고서 찾기
    const report = await db('generated_reports')
      .where('status', 'completed')
      .whereNotNull('pdf_path')
      .orderBy('report_id', 'desc')
      .first();
    
    if (!report) {
      console.log('❌ completed 상태의 보고서가 없습니다');
      process.exit(1);
    }
    
    console.log('\n✅ 테스트할 보고서:');
    console.log('- Report ID:', report.report_id);
    console.log('- Status:', report.status);
    console.log('- PDF Path:', report.pdf_path);
    console.log('- Generated By:', report.generated_by);
    
    // 파일 실제 존재 확인
    const fs = require('fs');
    const path = require('path');
    const fullPath = path.join(__dirname, report.pdf_path);
    
    if (fs.existsSync(fullPath)) {
      const stats = fs.statSync(fullPath);
      console.log('✅ 파일 존재:', (stats.size / 1024 / 1024).toFixed(2) + 'MB');
    } else {
      console.log('❌ 파일이 존재하지 않음:', fullPath);
    }
    
    process.exit(0);
  } catch (error) {
    console.error('❌ 에러:', error.message);
    process.exit(1);
  }
})();
